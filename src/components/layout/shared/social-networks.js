function SocialNetworks({ filterItems = [], navigation = false }) {
  let socialLinks = Object.entries(ORDERED_SOCIAL_NETWORK_LIST).map(([key, value]) => {
    return { title: key, href: value }
  })

  if (filterItems && filterItems.length > 0) {
    const socialLinksArray = [...socialLinks]
    socialLinks = socialLinksArray.filter((link) => filterItems.includes(link.title))
    socialLinks.sort((a, b) => filterItems.indexOf(a.title) - filterItems.indexOf(b.title))
  }

  return (
    <div className={classNames('exodus__social', { 'exodus__social--navigation': navigation })}>
      {socialLinks.map(({ title, href }) => (
        <a key={title} href={href} target="_blank">
          <svg fill="none" viewBox="0 0 32 32">
            <title>{title}</title>
            {iconSvg[title]}
          </svg>
        </a>
      ))}
    </div>
  )
}

export default SocialNetworks
